package com.ibm.iotf.model;

public class AssetData {

	private String BatteryLevel;
	private String BatteryVoltage;
	private String Date;
	private String DeviceProfile;
	private String LatX;
	private String LocationString;
	private String LongY;
	private String PacketType;
	private String PositionMethod;
	private String PowerSupplyVoltage;
	private String Satelites;
	private String isRoaming;
	public String getBatteryLevel() {
		return BatteryLevel;
	}
	public void setBatteryLevel(String batteryLevel) {
		BatteryLevel = batteryLevel;
	}
	public String getBatteryVoltage() {
		return BatteryVoltage;
	}
	public void setBatteryVoltage(String batteryVoltage) {
		BatteryVoltage = batteryVoltage;
	}
	public String getDate() {
		return Date;
	}
	public void setDate(String date) {
		Date = date;
	}
	public String getDeviceProfile() {
		return DeviceProfile;
	}
	public void setDeviceProfile(String deviceProfile) {
		DeviceProfile = deviceProfile;
	}
	public String getLatX() {
		return LatX;
	}
	public void setLatX(String latX) {
		LatX = latX;
	}
	public String getLocationString() {
		return LocationString;
	}
	public void setLocationString(String locationString) {
		LocationString = locationString;
	}
	public String getLongY() {
		return LongY;
	}
	public void setLongY(String longY) {
		LongY = longY;
	}
	public String getPacketType() {
		return PacketType;
	}
	public void setPacketType(String packetType) {
		PacketType = packetType;
	}
	public String getPositionMethod() {
		return PositionMethod;
	}
	public void setPositionMethod(String positionMethod) {
		PositionMethod = positionMethod;
	}
	public String getPowerSupplyVoltage() {
		return PowerSupplyVoltage;
	}
	public void setPowerSupplyVoltage(String powerSupplyVoltage) {
		PowerSupplyVoltage = powerSupplyVoltage;
	}
	public String getSatelites() {
		return Satelites;
	}
	public void setSatelites(String satelites) {
		Satelites = satelites;
	}
	public String getIsRoaming() {
		return isRoaming;
	}
	public void setIsRoaming(String isRoaming) {
		this.isRoaming = isRoaming;
	}
	
	
}
