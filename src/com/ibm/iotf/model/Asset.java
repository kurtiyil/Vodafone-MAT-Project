package com.ibm.iotf.model;

public class Asset {
	
	private String AssetId; 
	private String Name;
	
	public String getAssetId() {
		return AssetId;
	}
	public void setAssetId(String assetId) {
		AssetId = assetId;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	


}
